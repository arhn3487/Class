.MODEL SMALL
.STACK 100H

.DATA
    PROMPT_MSG DB 'Enter two digits: $'
    MSG_LARGER DB ' is larger in $'
    NUM1       DB ?
    NUM2       DB ?

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    MOV AH, 9
    LEA DX, PROMPT_MSG
    INT 21H

    MOV AH, 1
    INT 21H
    MOV NUM1, AL

    MOV AH, 1
    INT 21H

    MOV AH, 1
    INT 21H
    MOV NUM2, AL

    MOV AH, 2
    MOV DL, 0AH
    INT 21H
    MOV DL, 0DH
    INT 21H

    MOV AL, NUM1
    MOV BL, NUM2
    CMP AL, BL
    JGE FIRST_IS_LARGER

    MOV AL, BL
    JMP DISPLAY_LARGER

FIRST_IS_LARGER:

DISPLAY_LARGER:
    MOV AH, 2
    MOV DL, AL
    INT 21H

    MOV AH, 9
    LEA DX, MSG_LARGER
    INT 21H

    MOV AH, 4CH
    INT 21H

MAIN ENDP
END MAIN
